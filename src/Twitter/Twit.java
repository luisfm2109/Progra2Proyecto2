    private void listausuariosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_listausuariosMouseClicked
        String nombre= listausuarios.getSelectedValue();
        
            try{
                if(!nombre.equals("")){
                    target=nombre;
                    getTweets(nombre);

                    if(follows.isFollowing(currentUser, target)){
                        FollowUnfollow.setText("Unfollow");
                    }else{
                        FollowUnfollow.setText("Follow");
                    }
                    listausuarios.clearSelection();
                    buscarUser.setText("Buscar Usuario");
                    //Borrar users del Jlist
                    DefaultListModel<String> listModel2 = (DefaultListModel) listausuarios.getModel();
                    listModel2.removeAllElements();
                    displayUsuario.setText(usuarios.encontrarUsers(buscarUser.getText(),currentUser));
                    for(String s: usuarios.lista(buscarUser.getText(),currentUser)){
                        listModel2.addElement(s);
                    }
                    targetFollowers.setText("Followers: "+follows.cantidadFollowers(target));
                    targetFollowing.setText("Following: "+follows.cantidadFollowing(target));
                    nombreTarget.setText("@"+target);
                    if(follows.isFollower(currentUser, target)){
                        targetisFollowing.setText(target+" te esta siguiendo");
                        targetisFollowing.setForeground(Color.blue);
                    }else{
                        targetisFollowing.setText(target+" no te esta siguiendo");
                        targetisFollowing.setForeground(Color.red);
                    }
                    Menu(buscarProfile);
                }
            }catch(IOException e){
                System.out.println("Error: "+e.getMessage());
            }
            catch(BadLocationException e){
                System.out.println("Error: "+e.getMessage());
            }
            catch(NullPointerException e){
                System.out.println("Ningun usuario seleccionado");
            }
        
    }//GEN-LAST:event_listausuariosMouseClicked

