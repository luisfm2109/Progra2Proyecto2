    private void actualizarTimeline()throws IOException, BadLocationException{
        myMentions.setText("");
        for(Tweet t: twits.timeLine(currentUser)){
            SimpleAttributeSet attributeSet = new SimpleAttributeSet();  
            StyleConstants.setBold(attributeSet, true);
            Document doc = myMentions.getStyledDocument();
            
            myMentions.setCharacterAttributes(attributeSet, true);
            doc.insertString(doc.getLength()," "+t.username+" escribio: \n" , attributeSet);
            StyleConstants.setBold(attributeSet, false);
            doc = myMentions.getStyledDocument();
            doc.insertString(doc.getLength()," \""+t.contenido+"\"\n" , attributeSet);
            attributeSet = new SimpleAttributeSet();
            StyleConstants.setFontSize(attributeSet, 9);
            StyleConstants.setItalic(attributeSet, true);
            doc = myMentions.getStyledDocument();
            doc.insertString(doc.getLength()," el "+t.fecha+"\n\n" , attributeSet);
        }
        myMentions.setCaretPosition(0);
        
    }
    
