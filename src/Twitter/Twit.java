    private void crearCuentaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_crearCuentaActionPerformed
        String nombre = crearNombrecompleto.getText();
        String user = crearUsername.getText();
        String pass = crearPassword.getText();
        int edad = (int)crearEdad.getValue();
        char genero=' ';
        
        if(!nombre.equals("") && !user.equals("") && !pass.equals("")
                 && (crearMasculino.isSelected() || crearFemenino.isSelected()))
        {   
            if(crearMasculino.isSelected()){
                genero='M';
            }else{
                genero = 'F';
            }
            try{
                if(!usuarios.buscarUser(crearUsername.getText())){
                    usuarios.crearUser(nombre, user, pass, edad, genero);
                    currentUser=user;
                    crearNombrecompleto.setText(null);
                    crearUsername.setText(null);
                    crearPassword.setText(null);
                    crearEdad.setValue(1);
                    llenarcampos.setText(" ");
                    buttonGroup1.clearSelection();
                    JOptionPane.showMessageDialog(null, "Usuario creado exitosamente");
                    Menu(menuPrincipal);
                }else{
                    JOptionPane.showMessageDialog(null, "Usuario ya existe");
                }
            }
            catch(IOException e){
                System.out.println("Error: "+e.getMessage());
                }

        }else{
            llenarcampos.setText("Se deben llenar todos los campos");
        }
    }//GEN-LAST:event_crearCuentaActionPerformed

